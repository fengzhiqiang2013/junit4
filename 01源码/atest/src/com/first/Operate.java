package com.first;

/**
 * 计算器
 * @author aran
 *
 */
public class Operate {
	/**
	 * 两个整数相加
	 * @param a 
	 * @param b
	 * @return
	 */
	public int add(int a, int b){
		return a + b;
	}
	
	/**
	 * 两个整数相减
	 * @param a
	 * @param b
	 * @return
	 */
	public int sutract(int a, int b){
		return a - b;
	}
	/**
	 * 两个整数相减
	 * @param a
	 * @param b
	 * @return
	 */
	public int multiply(int a, int b){  
        return a * b;  
    }  
	
	/**
	 * 两个整数相除
	 * @param a
	 * @param b
	 * @return
	 */
    public int divide(int a, int b){  
        return a / b;  
    }
}
